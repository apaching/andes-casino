export const gameApi = {
  getGames: (
    offset = 0,
    limit = 24,
  ): Promise<{
    success: Boolean;
    games: Game[];
    hasMore: boolean;
    total: number;
    counts: Record<string, number>;
    error?: string;
  }> => {
    return new Promise((resolve) => {
      setTimeout(() => {
        const total = mockGames.length;
        const games = mockGames.slice(offset, offset + limit);
        const hasMore = offset + limit < total;

        const counts: Record<string, number> = mockGames.reduce(
          (acc: Record<string, number>, game) => {
            acc[game.category] = (acc[game.category] || 0) + 1;
            return acc;
          },
          {},
        );

        resolve({ success: true, games, hasMore, total, counts });
      }, 3000);
    });
  },
};
